-- [P0-3 FIX] 数据完整性触发器 (Public Asset Protection & Cross-User Isolation)
CREATE OR REPLACE FUNCTION public.check_public_asset_integrity()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  -- 1. 公共题目完整性 (error_questions)
  -- 规则：公共题不能引用私有学科
  IF TG_TABLE_NAME = 'error_questions' THEN
    IF NEW.user_id IS NULL AND NEW.subject_id IS NOT NULL THEN
      IF EXISTS (SELECT 1 FROM public.subjects s WHERE s.id = NEW.subject_id AND s.user_id IS NOT NULL) THEN
        RAISE EXCEPTION 'Public question cannot reference a private subject';
      END IF;
    END IF;
    
    -- [P2-FIX] 公共题目结构完整性: 公共题不应标记为 Fork
    IF NEW.user_id IS NULL AND NEW.forked_from IS NOT NULL THEN
      RAISE EXCEPTION 'Public questions cannot be forks';
    END IF;
    
    -- [P1-FIX] 私有题目隔离性 (error_questions)
    -- 规则：我的题目不能挂别人的私有学科 (FK 泄露防护)
    IF NEW.user_id IS NOT NULL AND NEW.subject_id IS NOT NULL THEN
      IF NOT EXISTS (
        SELECT 1 FROM public.subjects s 
        WHERE s.id = NEW.subject_id AND (s.user_id IS NULL OR s.user_id = NEW.user_id)
      ) THEN
        RAISE EXCEPTION 'Question cannot reference a subject owned by another user';
      END IF;
    END IF;
  END IF;

  -- 2. 标签关联完整性 (error_question_tags)
  IF TG_TABLE_NAME = 'error_question_tags' THEN
    -- 需要检查对应的题目是否是公共题目
    IF EXISTS (
      SELECT 1 FROM public.error_questions q 
      WHERE q.id = NEW.question_id AND q.user_id IS NULL
    ) THEN
      -- 如果是公共题目，tag 必须是公共 tag
      IF EXISTS (SELECT 1 FROM public.tags t WHERE t.id = NEW.tag_id AND t.user_id IS NOT NULL) THEN
        RAISE EXCEPTION 'Public question cannot reference a private tag';
      END IF;
    END IF;
  END IF;

  -- 3. [P1-FIX] 考试记录隔离性 (exam_records)
  IF TG_TABLE_NAME = 'exam_records' THEN
    IF NEW.subject_id IS NOT NULL THEN
      IF NOT EXISTS (
        SELECT 1 FROM public.subjects s 
        WHERE s.id = NEW.subject_id AND (s.user_id IS NULL OR s.user_id = NEW.user_id)
      ) THEN
        RAISE EXCEPTION 'Exam record cannot reference a subject owned by another user';
      END IF;
    END IF;
  END IF;

  RETURN NEW;
END $$;

