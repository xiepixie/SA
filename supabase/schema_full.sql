-- [V5.9] Helper: Check for updates (Diff View 1.0)
CREATE OR REPLACE FUNCTION public.check_fork_status(p_question_id UUID)
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER SET search_path = pg_catalog, public AS $$
DECLARE
  v_child public.error_questions%ROWTYPE;
  v_parent public.error_questions%ROWTYPE;
BEGIN
  SELECT * INTO v_child FROM public.error_questions WHERE id = p_question_id AND user_id = (SELECT auth.uid());
  IF NOT FOUND THEN RAISE EXCEPTION 'Question not found'; END IF;

  IF v_child.forked_from IS NULL THEN
    RETURN jsonb_build_object('is_fork', false);
  END IF;

  SELECT * INTO v_parent FROM public.error_questions WHERE id = v_child.forked_from;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('is_fork', true, 'parent_deleted', true);
  END IF;

  RETURN jsonb_build_object(
    'is_fork', true,
    'has_update', (v_parent.content_hash IS DISTINCT FROM v_child.last_synced_hash),
    'parent_hash', v_parent.content_hash,
    'last_synced_hash', v_child.last_synced_hash,
    -- Simple field diff for UI
    'diff_summary', jsonb_build_object(
       'title_changed', (v_parent.title IS DISTINCT FROM v_child.title),
       'content_changed', (v_parent.content IS DISTINCT FROM v_child.content),
       'answer_changed', (v_parent.correct_answer IS DISTINCT FROM v_child.correct_answer)
    )
  );
END $$;

GRANT EXECUTE ON FUNCTION public.check_fork_status(UUID) TO authenticated;

-- [V5.9] Action: PULL updates from parent
CREATE OR REPLACE FUNCTION public.sync_fork(p_question_id UUID)
RETURNS VOID LANGUAGE plpgsql SECURITY DEFINER SET search_path = pg_catalog, public AS $$
DECLARE
  v_uid UUID := (SELECT auth.uid());
  v_child public.error_questions%ROWTYPE;
  v_parent public.error_questions%ROWTYPE;
BEGIN
  SELECT * INTO v_child FROM public.error_questions WHERE id = p_question_id AND user_id = v_uid FOR UPDATE;
  IF NOT FOUND THEN RAISE EXCEPTION 'Question not found'; END IF;

  IF v_child.forked_from IS NULL THEN RAISE EXCEPTION 'Not a fork'; END IF;

  SELECT * INTO v_parent FROM public.error_questions WHERE id = v_child.forked_from;
  IF NOT FOUND THEN RAISE EXCEPTION 'Source question deleted'; END IF;

  -- Apply Update (Semantic Overwrite)
  UPDATE public.error_questions SET
    title = v_parent.title,
    content = v_parent.content,
    question_type = v_parent.question_type,
    difficulty = v_parent.difficulty,
    correct_answer = v_parent.correct_answer,
    correct_answer_text = v_parent.correct_answer_text,
    explanation = v_parent.explanation,
    image_url = v_parent.image_url,
    hints = v_parent.hints,
    -- Update Sync State
    last_synced_hash = v_parent.content_hash,
    updated_at = now()
  WHERE id = p_question_id;
END $$;

GRANT EXECUTE ON FUNCTION public.sync_fork(UUID) TO authenticated;

