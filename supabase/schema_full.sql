-- 1. Merge Subjects
CREATE OR REPLACE FUNCTION public.merge_subjects(p_source_id UUID, p_target_id UUID)
RETURNS UUID LANGUAGE plpgsql SECURITY DEFINER SET search_path = pg_catalog, public AS $$
DECLARE
  v_uid UUID := (SELECT auth.uid());
  v_affected_ids UUID[];
  v_affected_exam_ids UUID[]; -- [P1-4 FIX] 补充声明变量
  v_log_id UUID;
BEGIN
  -- [V5.8 Protection] 源/目标相同保护
  IF p_source_id = p_target_id THEN
    RAISE EXCEPTION 'Source and target subject must be different';
  END IF;

  -- [V5.8 Concurrency] 事务级咨询锁
  PERFORM pg_advisory_xact_lock(hashtext('merge_subjects' || v_uid::text));

  -- 1. 权限与状态校验
  IF NOT EXISTS (SELECT 1 FROM public.subjects WHERE id = p_source_id AND user_id = v_uid AND deleted_at IS NULL) OR
     NOT EXISTS (SELECT 1 FROM public.subjects WHERE id = p_target_id AND user_id = v_uid AND deleted_at IS NULL) THEN
    RAISE EXCEPTION 'Source or target subject not found or access denied';
  END IF;

  -- [P1-4] 收集受影响的题目与考试记录 (用于精准回滚)
  SELECT array_agg(id) INTO v_affected_ids FROM public.error_questions WHERE subject_id = p_source_id AND user_id = v_uid;
  SELECT array_agg(id) INTO v_affected_exam_ids FROM public.exam_records WHERE subject_id = p_source_id AND user_id = v_uid;

  -- 3. 执行迁移
  UPDATE public.error_questions SET subject_id = p_target_id WHERE subject_id = p_source_id AND user_id = v_uid;
  UPDATE public.exam_records SET subject_id = p_target_id WHERE subject_id = p_source_id AND user_id = v_uid;

  -- 4. 软删除源学科
  UPDATE public.subjects SET deleted_at = now() WHERE id = p_source_id;

  -- 5. 记录日志 (全量快照以支持物理清理后的极致审计)
  INSERT INTO public.management_logs (user_id, op_type, entity_type, source_id, target_id, affected_ids, metadata)
  SELECT 
    v_uid, 'merge', 'subject', p_source_id, p_target_id, 
    COALESCE(v_affected_ids, '{}'), 
    jsonb_build_object(
      'exam_record_ids', COALESCE(v_affected_exam_ids, '{}'),
      'full_snapshot', (SELECT to_jsonb(s.*) FROM public.subjects s WHERE s.id = p_source_id),
      'source_name', (SELECT name FROM public.subjects WHERE id = p_source_id),
      'target_name', (SELECT name FROM public.subjects WHERE id = p_target_id)
    )
  RETURNING id INTO v_log_id;

  RETURN v_log_id;
END $$;

-- 2. Merge Tags
CREATE OR REPLACE FUNCTION public.merge_tags(p_source_id UUID, p_target_id UUID)
RETURNS UUID LANGUAGE plpgsql SECURITY DEFINER SET search_path = pg_catalog, public AS $$
DECLARE
  v_uid UUID := (SELECT auth.uid());
  v_affected_ids UUID[];
  v_affected_card_ids UUID[]; -- [V5.8] 追踪 JSONB 标签受影响的卡片
  v_log_id UUID;
  v_now_ts TIMESTAMPTZ := now();
BEGIN
  -- [V5.8 Protection]
  IF p_source_id = p_target_id THEN
    RAISE EXCEPTION 'Source and target tag must be different';
  END IF;

  -- [V5.8 Concurrency]
  PERFORM pg_advisory_xact_lock(hashtext('merge_tags' || v_uid::text));

  IF NOT EXISTS (SELECT 1 FROM public.tags WHERE id = p_source_id AND user_id = v_uid AND deleted_at IS NULL) OR
     NOT EXISTS (SELECT 1 FROM public.tags WHERE id = p_target_id AND user_id = v_uid AND deleted_at IS NULL) THEN
    RAISE EXCEPTION 'Source or target tag not found or access denied';
  END IF;

  -- 收集受影响题目（仅那些目前还没被打上 target_id 标签的题目）
  SELECT array_agg(question_id) INTO v_affected_ids 
  FROM public.error_question_tags 
  WHERE tag_id = p_source_id 
    AND question_id NOT IN (SELECT question_id FROM public.error_question_tags WHERE tag_id = p_target_id);

  -- 执行合并逻辑
  -- 1. 将源标签对应的题目，且目标标签未覆盖的，插入目标标签
  -- [V5.8 Note] 依赖 v_now_ts 的高精度与 pg_advisory_xact_lock 的串行化保证
  INSERT INTO public.error_question_tags (question_id, tag_id, created_at)
  SELECT question_id, p_target_id, v_now_ts FROM public.error_question_tags WHERE tag_id = p_source_id
  ON CONFLICT (question_id, tag_id) DO NOTHING;
  
  -- 2. 删除源标签的关联
  DELETE FROM public.error_question_tags WHERE tag_id = p_source_id;

  -- [V5.8 Deep Merge] 同步更新 cards 表中的个人标签 JSONB 数组 (Overlay Layer)
  -- 逻辑：替换数组中的 UUID 并去重
  WITH updated_cards AS (
    UPDATE public.cards SET 
      personal_tags = (
        SELECT jsonb_agg(DISTINCT val)
        FROM (
          SELECT CASE WHEN (v #>> '{}') = p_source_id::text THEN to_jsonb(p_target_id::text) ELSE v END AS val
          FROM jsonb_array_elements(personal_tags) AS v
        ) s
      ),
      updated_at = v_now_ts
    WHERE personal_tags ? p_source_id::text AND user_id = v_uid
    RETURNING id
  )
  SELECT array_agg(id) INTO v_affected_card_ids FROM updated_cards;

  -- 3. 软删除源标签
  UPDATE public.tags SET deleted_at = v_now_ts WHERE id = p_source_id;

  -- 记录日志 (全量快照以支持物理清理后的极致审计)
  INSERT INTO public.management_logs (user_id, op_type, entity_type, source_id, target_id, affected_ids, metadata)
  SELECT 
    v_uid, 'merge', 'tag', p_source_id, p_target_id, 
    COALESCE(v_affected_ids, '{}'), 
    jsonb_build_object(
      'merge_timestamp', v_now_ts,
      'affected_card_ids', COALESCE(v_affected_card_ids, '{}'),
      'full_snapshot', (SELECT to_jsonb(t.*) FROM public.tags t WHERE t.id = p_source_id),
      'source_name', (SELECT name FROM public.tags WHERE id = p_source_id),
      'target_name', (SELECT name FROM public.tags WHERE id = p_target_id)
    )
  RETURNING id INTO v_log_id;

  RETURN v_log_id;
END $$;

-- 3. Undo Management Operation
CREATE OR REPLACE FUNCTION public.undo_management_op(p_log_id UUID)
RETURNS BOOLEAN LANGUAGE plpgsql SECURITY DEFINER SET search_path = pg_catalog, public AS $$
DECLARE
  v_uid UUID := (SELECT auth.uid());
  v_log public.management_logs%ROWTYPE;
BEGIN
  -- [V5.8 Concurrency]
  PERFORM pg_advisory_xact_lock(hashtext('undo_management_op' || v_uid::text));

  SELECT * INTO v_log FROM public.management_logs WHERE id = p_log_id AND user_id = v_uid AND undone_at IS NULL;
  IF NOT FOUND THEN RAISE EXCEPTION 'Operation log not found or already undone'; END IF;

  -- 恢复实体
  IF v_log.entity_type = 'subject' THEN
    IF NOT EXISTS (SELECT 1 FROM public.subjects WHERE id = v_log.source_id) THEN
      RAISE EXCEPTION 'Cannot undo: Source subject (%) has been physically purged', v_log.source_id;
    END IF;
    UPDATE public.subjects SET deleted_at = NULL WHERE id = v_log.source_id;
    IF v_log.op_type = 'merge' THEN
      -- 还原题目学科归属
      UPDATE public.error_questions SET subject_id = v_log.source_id WHERE id = ANY(v_log.affected_ids);
      -- [P1-4] 还原考试记录 (基于 metadata 中的 precise ids)
      UPDATE public.exam_records SET subject_id = v_log.source_id 
      WHERE id = ANY(ARRAY(SELECT jsonb_array_elements_text(v_log.metadata->'exam_record_ids'))::UUID[]);
    END IF;
  ELSIF v_log.entity_type = 'tag' THEN
    IF NOT EXISTS (SELECT 1 FROM public.tags WHERE id = v_log.source_id) THEN
      RAISE EXCEPTION 'Cannot undo: Source tag (%) has been physically purged', v_log.source_id;
    END IF;
    UPDATE public.tags SET deleted_at = NULL WHERE id = v_log.source_id;
    IF v_log.op_type = 'merge' THEN
      -- [P1-5] 还原关系：重新建立与 source_id 的关联 (增加存在性校验防止 FK 失败)
      INSERT INTO public.error_question_tags (question_id, tag_id)
      SELECT qid, v_log.source_id
      FROM unnest(v_log.affected_ids) AS qid
      WHERE EXISTS (SELECT 1 FROM public.error_questions q WHERE q.id = qid);
      
      -- 2. 清理合并产生的多余关联：仅删除在 merge 发生瞬间创建的 target_id 关联
      -- [V5.8 Performance] 结合 created_at 与 affected_ids 提供精准识别，无需额外 source_log_id 列
      DELETE FROM public.error_question_tags 
      WHERE tag_id = v_log.target_id 
        AND question_id = ANY(v_log.affected_ids)
        AND created_at = (v_log.metadata->>'merge_timestamp')::TIMESTAMPTZ;

      -- 3. [V5.8 Deep Undo] 还原 cards 表中的个人标签
      IF v_log.metadata ? 'affected_card_ids' THEN
        UPDATE public.cards SET 
          personal_tags = (
            SELECT jsonb_agg(DISTINCT val)
            FROM (
              SELECT CASE WHEN (v #>> '{}') = v_log.target_id::text THEN v_log.source_id::text::jsonb ELSE v END AS val
              FROM jsonb_array_elements(personal_tags) AS v
            ) s
          )
        WHERE id = ANY(ARRAY(SELECT jsonb_array_elements_text(v_log.metadata->'affected_card_ids'))::UUID[]);
      END IF;
    END IF;
  END IF;

  -- [V5.8 Audit] 任务完成，标记已撤销，不再物理删除
  UPDATE public.management_logs SET 
    undone_at = now(),
    undone_by = v_uid,
    undo_metadata = jsonb_build_object('performed_at', now())
  WHERE id = p_log_id;

  RETURN TRUE;
END $$;

