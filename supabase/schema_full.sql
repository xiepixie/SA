-- [V5.3] FSRS 权重归档表 (Content-Addressable Storage)
-- 目的: 1. 100% 还原历史参数 2. 全局去重 (不同用户使用相同权重只存一份)
CREATE TABLE public.fsrs_configs (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    hash TEXT UNIQUE NOT NULL,      -- [P1-1] sha256 content hash (64 chars)
    weights JSONB NOT NULL,         -- 原始权重数据 [0.1, 0.2, ...]
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    
    -- [V5.3 Security] 权重长度校验
    CONSTRAINT fsrs_configs_weights_check CHECK (
        jsonb_typeof(weights) = 'array' AND jsonb_array_length(weights) = 21
    ),
    
    -- [P1-1 Data Integrity] 确保哈希与权重自洽，防止脏数据注入 (Using sha256)
    -- 注意: hash 基于 JSONB 规范化后的 text (key 排序 & 统一数值格式)，确保哈希稳定性
    CONSTRAINT fsrs_configs_hash_self_consistency CHECK (
        hash = encode(extensions.digest(weights::text, 'sha256'), 'hex')
    ),
    
    -- [P1-1 FIX] 样式/长度强约束，防止脏数据注入
    CONSTRAINT fsrs_configs_hash_format_check CHECK (hash ~ '^[0-9a-f]{64}$')
);

COMMENT ON TABLE public.fsrs_configs IS 'Deduplicated FSRS parameter configurations for full historical restoration';

-- [V5.4] 权重表权限收口：禁止公有访问
REVOKE ALL ON public.fsrs_configs FROM PUBLIC;
