-- 4. Purge Soft Deleted Data (Maintenance)
-- [V5.8] 周期性安全清理逻辑：
-- 1. 只有当没有题目/记录引用时才执行物理删除 (Dependency-Aware)
-- 2. 结合 RPC 中的名称快照，即使物理删除，审计日志仍可读
CREATE OR REPLACE FUNCTION public.purge_soft_deleted_data(p_days_threshold INTEGER DEFAULT 30)
RETURNS JSONB LANGUAGE plpgsql SECURITY DEFINER SET search_path = pg_catalog, public AS $$
DECLARE
  v_subject_count INTEGER := 0;
  v_tag_count INTEGER := 0;
  v_log_count INTEGER := 0;
  v_threshold TIMESTAMPTZ := now() - (p_days_threshold || ' days')::INTERVAL;
BEGIN
  -- 1. 清理学科 (安全物理删除)
  -- 条件：软删除超过阈值 + 无任何题目引用 + 无任何考试记录引用
  DELETE FROM public.subjects s
  WHERE s.deleted_at IS NOT NULL 
    AND s.deleted_at < v_threshold
    AND NOT EXISTS (SELECT 1 FROM public.error_questions q WHERE q.subject_id = s.id)
    AND NOT EXISTS (SELECT 1 FROM public.exam_records e WHERE e.subject_id = s.id);
  GET DIAGNOSTICS v_subject_count = ROW_COUNT;

  -- 2. 清理标签 (安全物理删除)
  -- 条件：软删除超过阈值 + 关联表无引用
  DELETE FROM public.tags t
  WHERE t.deleted_at IS NOT NULL 
    AND t.deleted_at < v_threshold
    AND NOT EXISTS (SELECT 1 FROM public.error_question_tags eqt WHERE eqt.tag_id = t.id);
  GET DIAGNOSTICS v_tag_count = ROW_COUNT;

  -- 3. 清理已撤销的审计日志 (物理删除)
  DELETE FROM public.management_logs WHERE undone_at IS NOT NULL AND undone_at < v_threshold;
  GET DIAGNOSTICS v_log_count = ROW_COUNT;

  RETURN jsonb_build_object(
    'subjects_purged', v_subject_count,
    'tags_purged', v_tag_count,
    'logs_purged', v_log_count,
    'threshold_used', v_threshold,
    'status', 'safety_checks_performed'
  );
END $$;

