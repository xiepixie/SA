-- RLS: Follows Question Ownership
-- 1. Read: If I can see the question, I can see its tags (only if tag is NOT soft-deleted)
CREATE POLICY "qtags_read_shared" ON public.error_question_tags FOR SELECT TO authenticated
USING (
  EXISTS (SELECT 1 FROM public.error_questions q WHERE q.id = question_id AND (q.user_id IS NULL OR q.user_id = (SELECT auth.uid())))
  AND EXISTS (SELECT 1 FROM public.tags t WHERE t.id = tag_id AND t.deleted_at IS NULL) -- [P2-7] Soft-delete Check
);

-- [P1-FIX] 2. Write: I can only tag MY questions with VISIBLE tags
-- 防止挂别人的私有 tag 到自己题目
CREATE POLICY "qtags_write_mine" ON public.error_question_tags FOR INSERT TO authenticated
WITH CHECK (
  -- Question must be mine
  EXISTS (SELECT 1 FROM public.error_questions q WHERE q.id = question_id AND q.user_id = (SELECT auth.uid()))
  -- Tag must be visible (public or mine) AND NOT soft-deleted
  AND EXISTS (SELECT 1 FROM public.tags t WHERE t.id = tag_id AND (t.user_id IS NULL OR t.user_id = (SELECT auth.uid())) AND t.deleted_at IS NULL)
);

CREATE POLICY "qtags_del_mine" ON public.error_question_tags FOR DELETE TO authenticated
USING (
  EXISTS (SELECT 1 FROM public.error_questions q WHERE q.id = question_id AND q.user_id = (SELECT auth.uid()))
);

