-- 4. 核心 RPC & 触发器 (Logic)
-- ============================================

-- [V5.8 Security Model]
-- 所有 SECURITY DEFINER 函数必须：
-- 1. 明确 SET search_path = pg_catalog, public (防止搜索路径劫持)
-- 2. 内部执行 auth.uid() 鉴权 (绕过 RLS)
-- 3. 仅限 authenticated 或 service_role 调用
-- 4. 推荐由 postgres 角色拥有
CREATE OR REPLACE FUNCTION public.fork_question_to_private(p_question_id UUID) 
RETURNS UUID 
LANGUAGE plpgsql 
SECURITY DEFINER 
SET search_path = pg_catalog, public
AS $$
DECLARE
  v_uid UUID := (SELECT auth.uid());
  v_new_q_id UUID;
  v_origin public.error_questions%ROWTYPE;
BEGIN
  -- [P0-FIX] 可见性检查: 只能 fork 公共题或自己的题
  SELECT * INTO v_origin 
  FROM public.error_questions 
  WHERE id = p_question_id 
    AND (user_id IS NULL OR user_id = v_uid)
    AND is_archived = false; -- [V5.5 Logic] 禁止 Fork 已归档题目
    
  IF v_origin.id IS NULL THEN 
    RAISE EXCEPTION 'Question not found, access denied, or archived'; 
  END IF;

  -- [V5.6 Consistency] 检查题目所属学科是否已被软删除
  IF v_origin.subject_id IS NOT NULL THEN
    IF EXISTS (SELECT 1 FROM public.subjects WHERE id = v_origin.subject_id AND deleted_at IS NOT NULL) THEN
      RAISE EXCEPTION 'Question belongs to a deleted subject and cannot be forked';
    END IF;
  END IF;

  -- [V5.5 Logic Close-loop] 禁止 Fork 已归档题目
  IF v_origin.is_archived THEN
    RAISE EXCEPTION 'Cannot fork an archived question';
  END IF;
  
  -- 如果已经是自己的题，不需要 fork
  IF v_origin.user_id = v_uid THEN
    RAISE EXCEPTION 'Cannot fork your own question';
  END IF;

  -- [P1-FIX] 幂等性检查：防止重复 fork (Optimization & Race Condition Handling)
  -- 1. Try to fetch existing (Idempotency)
  SELECT id INTO v_new_q_id FROM public.error_questions 
  WHERE user_id = v_uid AND forked_from = p_question_id;
  
  -- 2. If not found, try to insert (Safe Concurrency)
  IF v_new_q_id IS NULL THEN
    BEGIN
      INSERT INTO public.error_questions (
        user_id, title, content, question_type, difficulty,
        correct_answer, correct_answer_text, explanation,
        correct_answer_image_url, explanation_image_url, image_url,
        hints, metadata, subject_id, forked_from
      )
      VALUES (
        v_uid, v_origin.title, v_origin.content, v_origin.question_type, v_origin.difficulty,
        v_origin.correct_answer, v_origin.correct_answer_text, v_origin.explanation,
        v_origin.correct_answer_image_url, v_origin.explanation_image_url, v_origin.image_url,
        v_origin.hints, 
        COALESCE(v_origin.metadata, '{}'::jsonb) || jsonb_build_object('forked_at', now()::text),
        v_origin.subject_id, p_question_id
      )
      RETURNING id INTO v_new_q_id;
      
      -- Clone Tags (only if we created the question)
      INSERT INTO public.error_question_tags (question_id, tag_id)
      SELECT v_new_q_id, eqt.tag_id 
      FROM public.error_question_tags eqt
      JOIN public.tags t ON t.id = eqt.tag_id
      WHERE eqt.question_id = p_question_id
        AND (t.user_id IS NULL OR t.user_id = v_uid);
        
    EXCEPTION WHEN unique_violation THEN
      -- Race condition caught: fetch the one created by the other tx
      SELECT id INTO v_new_q_id FROM public.error_questions 
      WHERE user_id = v_uid AND forked_from = p_question_id;
    END;
  END IF;
  
  -- [P0-FIX] Card Logic Optimization (Robust Concurrency)
  -- 逻辑：确保用户拥有 v_new_q_id 的卡片，且不残留 p_question_id 的卡片
  
  -- A. 检查新卡是否存在 (可能是之前的 fork 或并发 tx 创建的)
  IF EXISTS (SELECT 1 FROM public.cards WHERE user_id = v_uid AND question_id = v_new_q_id) THEN
      -- 新卡已存在：只需清理旧卡 (如果存在)
      DELETE FROM public.cards WHERE user_id = v_uid AND question_id = p_question_id;
  ELSE
      -- 新卡不存在：尝试将旧卡迁移为新卡
      UPDATE public.cards 
      SET question_id = v_new_q_id, updated_at = NOW()
      WHERE user_id = v_uid AND question_id = p_question_id;
      
      -- 如果没有旧卡可迁移，则创建新卡
      IF NOT FOUND THEN
        INSERT INTO public.cards (user_id, question_id, state)
        VALUES (v_uid, v_new_q_id, 0) -- 0 = New
        ON CONFLICT (user_id, question_id) DO NOTHING; -- Final safety net
      END IF;
  END IF;
  
  RETURN v_new_q_id;
END $$;

REVOKE ALL ON FUNCTION public.fork_question_to_private(UUID) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.fork_question_to_private(UUID) TO authenticated;

-- [V5.2] Function: Atomic Review Storage (Pure Persistence Layer)
-- 职责分离：
--   - 算法计算：Elysia + Rust (fsrs-rs)
--   - 本函数只负责：锁 + 校验 + 原子写入
-- 保证:
--   1. FOR UPDATE 锁防止并发更新
--   2. 事务原子性：log + card 同时成功/失败
--   3. 幂等性：相同 client_request_id 返回缓存结果
-- [V5.4] Function: Atomic Review Storage (Pure Persistence Layer)
-- 职责分离：
--   - 算法计算：Elysia + Rust (fsrs-rs)
--   - 本函数只负责：锁 + 校验 + 原子写入
-- 保证:
--   1. FOR UPDATE 锁防止并发更新
--   2. 事务原子性：log + card 同时成功/失败
--   3. 幂等性：相同 client_request_id 返回完整 post_state 结果
CREATE OR REPLACE FUNCTION public.submit_review(
  -- 基础参数
  p_user_id UUID,                        -- [V5.4] 显式传入 UID，面向 service_role 
  p_card_id UUID,
  p_rating INTEGER,
  
  -- 算法计算结果 (由 Elysia/Rust 传入)
  p_new_state SMALLINT,
  p_new_stability DOUBLE PRECISION,
  p_new_difficulty DOUBLE PRECISION,
  p_new_due TIMESTAMPTZ,
  p_scheduled_days INTEGER,
  
  -- 元数据与权重
  p_duration_ms INTEGER DEFAULT NULL,
  p_algo_version TEXT DEFAULT 'fsrs_v5',
  p_weights JSONB DEFAULT NULL,          -- [V5.3] 传入原始权重，DB 自动去重归档
  p_client_request_id UUID DEFAULT NULL
) RETURNS JSONB
LANGUAGE plpgsql 
SECURITY DEFINER 
SET search_path = pg_catalog, public
AS $$
DECLARE 
  v_card public.cards%ROWTYPE;
  v_now TIMESTAMPTZ := now();
  v_elapsed_days INTEGER;
  v_existing_log public.review_logs%ROWTYPE;
  v_weights_hash TEXT;
  v_config_id INTEGER;
  v_post_state JSONB;
BEGIN
  -- =========================================
  -- 0. 鲁棒性校验 (V5.5)
  -- =========================================
  IF p_user_id IS NULL THEN
    RAISE EXCEPTION 'p_user_id is required for service_role calls';
  END IF;

  -- =========================================
  -- 1. 参数校验
  -- =========================================
  IF p_rating NOT BETWEEN 1 AND 4 THEN
    RAISE EXCEPTION 'Rating must be between 1 and 4';
  END IF;
  
  IF p_new_state NOT BETWEEN 0 AND 3 THEN
    RAISE EXCEPTION 'State must be between 0 and 3';
  END IF;
  
  IF p_new_difficulty NOT BETWEEN 1 AND 10 THEN
    RAISE EXCEPTION 'Difficulty must be between 1 and 10';
  END IF;
  
  IF p_new_stability < 0 THEN
    RAISE EXCEPTION 'Stability must be non-negative';
  END IF;

  -- [V5.4] 权重结构校验补齐
  IF p_weights IS NOT NULL AND (jsonb_typeof(p_weights) <> 'array' OR jsonb_array_length(p_weights) <> 21) THEN
    RAISE EXCEPTION 'FSRS weights must be an array of length 21';
  END IF;

  -- =========================================
  -- 2. 幂等性检查
  -- =========================================
  IF p_client_request_id IS NOT NULL THEN
    SELECT * INTO v_existing_log 
    FROM public.review_logs 
    WHERE user_id = p_user_id AND client_request_id = p_client_request_id;
    
    IF FOUND THEN
      -- [V5.4] 返回之前保存的完整 post_state，确保响应一致性
      RETURN v_existing_log.post_state || jsonb_build_object(
        'success', true,
        'idempotent', true,
        'message', 'Duplicate request, returning cached result'
      );
    END IF;
  END IF;

  -- =========================================
  -- 3. 权重归档 (Content-Addressable)
  -- =========================================
  IF p_weights IS NOT NULL THEN
    -- [P1-1] 改用 sha256
    v_weights_hash := encode(extensions.digest(p_weights::text, 'sha256'), 'hex');
    
    -- 原子性查找或插入归档记录
    WITH ins AS (
      INSERT INTO public.fsrs_configs (hash, weights)
      VALUES (v_weights_hash, p_weights)
      ON CONFLICT (hash) DO NOTHING
      RETURNING id
    )
    SELECT id INTO v_config_id FROM ins
    UNION ALL
    SELECT id FROM public.fsrs_configs WHERE hash = v_weights_hash
    LIMIT 1;
  END IF;

  -- =========================================
  -- 4. 锁定卡片行
  -- =========================================
  SELECT * INTO v_card 
  FROM public.cards 
  WHERE id = p_card_id AND user_id = p_user_id
  FOR UPDATE;
  
  IF NOT FOUND THEN 
    RAISE EXCEPTION 'Card not found or access denied'; 
  END IF;

  v_elapsed_days := GREATEST(0, 
    EXTRACT(DAY FROM (v_now - COALESCE(v_card.last_review, v_card.created_at)))::INTEGER
  );

  -- 构造 post_state（用于幂等返回及分析）
  v_post_state := jsonb_build_object(
    'card_id', p_card_id,
    'new_state', p_new_state,
    'new_due', p_new_due,
    'scheduled_days', p_scheduled_days,
    'config_id', v_config_id,
    'algo_version', p_algo_version
  );

  -- =========================================
  -- 5. 写入复习日志
  -- =========================================
  INSERT INTO public.review_logs (
    card_id, rating, source,
    state, stability, difficulty,
    elapsed_days, last_elapsed_days, scheduled_days,
    review_duration_ms, review, algo_version, config_id,
    client_request_id, post_state
  ) VALUES (
    p_card_id, p_rating, 'review',
    v_card.state,
    v_card.stability,
    v_card.difficulty,
    v_elapsed_days, v_card.elapsed_days, p_scheduled_days,
    p_duration_ms, v_now, p_algo_version, v_config_id,
    p_client_request_id, v_post_state
  );

  -- =========================================
  -- 6. 更新卡片状态
  -- =========================================
  UPDATE public.cards SET
    state = p_new_state,
    stability = p_new_stability,
    difficulty = p_new_difficulty,
    due = p_new_due,
    last_review = v_now,
    elapsed_days = v_elapsed_days,
    scheduled_days = p_scheduled_days,
    reps = reps + 1,
    lapses = CASE WHEN p_rating = 1 THEN lapses + 1 ELSE lapses END,
    updated_at = v_now
  WHERE id = p_card_id;

  RETURN v_post_state || jsonb_build_object('success', true, 'idempotent', false);
END $$;

