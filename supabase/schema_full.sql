-- [V5.9] Content Hashing Trigger (Immutable Content Fingerprint)
-- 用于检测实质内容变更 (Diff Detection)
CREATE OR REPLACE FUNCTION public.calculate_question_content_hash()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  -- [P2-6] 扩展哈希覆盖范围：包含解释、难度、备选文本及资源链接
  -- 确保任何深度变更都能触发 Fork Sync 提醒
  NEW.content_hash := encode(extensions.digest(
    jsonb_build_array(
      NEW.title, 
      NEW.content, 
      NEW.question_type, 
      NEW.correct_answer,
      NEW.difficulty,
      NEW.explanation,
      NEW.correct_answer_text,
      NEW.image_url, 
      NEW.explanation_image_url,
      NEW.correct_answer_image_url,
      NEW.hints->'choices' -- Version controlled choices
    )::text,
    'sha256'
  ), 'hex');

  -- 如果是新 Fork (Insert) 且有来源，初始化同步状态
  IF TG_OP = 'INSERT' AND NEW.forked_from IS NOT NULL THEN
    -- 尝试获取父题当前的 hash (如果事务中父题可见)
    NEW.last_synced_hash := (SELECT content_hash FROM public.error_questions WHERE id = NEW.forked_from);
  END IF;

  RETURN NEW;
END $$;

CREATE TRIGGER trg_questions_calc_hash
BEFORE INSERT OR UPDATE OF 
  title, content, question_type, correct_answer, difficulty, explanation,
  correct_answer_text, image_url, explanation_image_url, correct_answer_image_url, hints, forked_from
ON public.error_questions
FOR EACH ROW EXECUTE FUNCTION public.calculate_question_content_hash();

-- [Migration] 一次性补齐历史数据的 content_hash (针对旧版本平滑升级)
-- 确保 Parent 的 hash 存在，使 Fork Sync 机制能够正常初始化 last_synced_hash
UPDATE public.error_questions
SET content_hash = encode(extensions.digest(
  jsonb_build_array(
    title, content, question_type, correct_answer, difficulty, explanation,
    correct_answer_text, image_url, explanation_image_url, correct_answer_image_url,
    hints->'choices'
  )::text, 'sha256'
),'hex')
WHERE content_hash IS NULL;

-- [P1-FIX] correct_answer 结构校验触发器
-- 确保 correct_answer 对于有答案的题型包含必要字段
CREATE OR REPLACE FUNCTION public.validate_correct_answer()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  -- 允许空对象用于草稿，但非空时必须符合结构标准
  IF NEW.correct_answer = '{}'::jsonb THEN
    RETURN NEW;
  END IF;

  -- 校验 type 字段（必须存在）
  IF NOT (NEW.correct_answer ? 'type') THEN
    RAISE EXCEPTION 'correct_answer must contain a "type" field';
  END IF;

  -- [P2-FIX] 校验 correct_answer.type 与 question_type 的强一致性
  IF (NEW.correct_answer->>'type') IS DISTINCT FROM (NEW.question_type::text) THEN
    RAISE EXCEPTION 'correct_answer.type ("%") must match question_type ("%")', NEW.correct_answer->>'type', NEW.question_type;
  END IF;

  -- choice 校验
  IF NEW.question_type = 'choice' THEN
    -- [P2-5] 增加非空与唯一性校验
    IF NOT (
       (NEW.correct_answer ? 'choice_ids' AND jsonb_typeof(NEW.correct_answer->'choice_ids') = 'array' AND jsonb_array_length(NEW.correct_answer->'choice_ids') > 0) 
       OR NEW.correct_answer ? 'choice_id'
    ) THEN
      RAISE EXCEPTION 'Choice answer requires non-empty choice_ids (array) or choice_id';
    END IF;
    
    -- [P1-1] 增加选择项 ID 唯一性强校验
    IF NEW.correct_answer ? 'choice_ids' THEN
      IF jsonb_array_length(NEW.correct_answer->'choice_ids') <>
         (SELECT count(DISTINCT x) FROM jsonb_array_elements_text(NEW.correct_answer->'choice_ids') x) THEN
        RAISE EXCEPTION 'choice_ids must be unique';
      END IF;
    END IF;
  END IF;
  
  -- fill_blank 校验
  IF NEW.question_type = 'fill_blank' THEN
    IF NOT (NEW.correct_answer ? 'blanks' AND jsonb_typeof(NEW.correct_answer->'blanks') = 'array' AND jsonb_array_length(NEW.correct_answer->'blanks') > 0) THEN
      RAISE EXCEPTION 'Fill-blank answer requires non-empty blanks (array)';
    END IF;
  END IF;
  
  -- short_answer 校验
  IF NEW.question_type = 'short_answer' THEN
    IF NOT (NEW.correct_answer ? 'answers' AND jsonb_typeof(NEW.correct_answer->'answers') = 'array' AND jsonb_array_length(NEW.correct_answer->'answers') > 0) THEN
        RAISE EXCEPTION 'Short-answer requires non-empty answers (array)';
    END IF;
    
    -- [P1-1] 增加答案类型与非重复校验
    IF EXISTS (SELECT 1 FROM jsonb_array_elements(NEW.correct_answer->'answers') x WHERE jsonb_typeof(x) <> 'string') THEN
      RAISE EXCEPTION 'Short-answer entries must be strings';
    END IF;

    IF jsonb_array_length(NEW.correct_answer->'answers') <>
       (SELECT count(DISTINCT x) FROM jsonb_array_elements_text(NEW.correct_answer->'answers') x) THEN
      RAISE EXCEPTION 'Short-answer entries must be unique';
    END IF;
  END IF;

  RETURN NEW;
END $$;

CREATE TRIGGER trg_validate_correct_answer
BEFORE INSERT OR UPDATE ON public.error_questions
FOR EACH ROW EXECUTE FUNCTION public.validate_correct_answer();

