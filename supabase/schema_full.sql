-- [V5.2] Function: Claim Import Job (Worker Contention Safe)
-- Worker 使用 FOR UPDATE SKIP LOCKED 原子抢占任务
-- 返回: 抢到的任务详情，或 NULL（无可用任务）
CREATE OR REPLACE FUNCTION public.claim_import_job(
  p_worker_id TEXT,                     -- Worker 标识 (hostname/pod-id)
  p_lock_duration_minutes INTEGER DEFAULT 10
) RETURNS JSONB
LANGUAGE plpgsql 
SECURITY DEFINER 
SET search_path = pg_catalog, public
AS $$
DECLARE 
  v_job public.import_jobs%ROWTYPE;
  v_now TIMESTAMPTZ := now();
BEGIN
  -- 原子抢占：SELECT FOR UPDATE SKIP LOCKED
  -- [P2-4 Logic Fix] 包含 'partial' 状态，支持断点续传
  -- [P1-3 Patch] 增加重试门禁校验
  SELECT * INTO v_job
  FROM public.import_jobs
  WHERE (
    status = 'queued' 
    OR (status = 'partial' AND (next_retry_at IS NULL OR next_retry_at <= v_now))
    OR (status = 'failed' AND attempts < max_attempts AND (next_retry_at IS NULL OR next_retry_at <= v_now))
    -- [V5.5 Robustness] 处理 locked_at 可能为 NULL 的边界情况
    OR (status = 'running' AND (locked_at IS NULL OR locked_at < v_now - (p_lock_duration_minutes || ' minutes')::INTERVAL)) 
  )
  ORDER BY created_at ASC
  LIMIT 1
  FOR UPDATE SKIP LOCKED;
  
  IF NOT FOUND THEN
    RETURN NULL;
  END IF;
  
  -- 更新任务状态为 running 并锁定
  UPDATE public.import_jobs SET
    status = 'running',
    locked_at = v_now,
    locked_by = p_worker_id,
    attempts = attempts + 1,
    started_at = COALESCE(started_at, v_now),
    updated_at = v_now
  WHERE id = v_job.id;
  
  -- 返回任务详情
  RETURN jsonb_build_object(
    'job_id', v_job.id,
    'user_id', v_job.user_id,
    'file_path', v_job.file_path,
    'file_name', v_job.file_name,
    'file_type', v_job.file_type,
    'checkpoint_row', v_job.checkpoint_row,
    'config', v_job.config,
    'attempt', v_job.attempts + 1
  );
END $$;

