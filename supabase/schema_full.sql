-- [P0-FIX] review_logs 冗余字段强一致性触发器
-- 确保 user_id 和 question_id 与 card_id 的归属严格一致，防止前端传入脏数据
CREATE OR REPLACE FUNCTION public.sync_review_log_denorm()
RETURNS trigger LANGUAGE plpgsql AS $$
DECLARE 
  v_card_user_id UUID; 
  v_card_question_id UUID;
BEGIN
  IF NEW.card_id IS NULL THEN
    RETURN NEW;
  END IF;

  SELECT user_id, question_id INTO v_card_user_id, v_card_question_id
  FROM public.cards WHERE id = NEW.card_id;
  
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Card with id % not found', NEW.card_id;
  END IF;

  -- 自动填充 & 强一致性校验
  NEW.user_id := COALESCE(NEW.user_id, v_card_user_id);
  NEW.question_id := COALESCE(NEW.question_id, v_card_question_id);

  IF NEW.user_id <> v_card_user_id THEN
    RAISE EXCEPTION 'User ID mismatch between log and card';
  END IF;
  
  IF NEW.question_id <> v_card_question_id THEN
    RAISE EXCEPTION 'Question ID mismatch between log and card';
  END IF;

  RETURN NEW;
END $$;

CREATE TRIGGER trg_logs_sync_denorm
BEFORE INSERT ON public.review_logs
FOR EACH ROW EXECUTE FUNCTION public.sync_review_log_denorm();

